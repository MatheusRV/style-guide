          <h3>C</h3>
          <h4>CAC - Customer Acquisition Cost</h4>
          <p>Essa é a métrica de quanto custa um cliente, somando os gastos com vendas e marketing. Para fazer o cálculo simplificado, estabeleça um período — mensal, trimestral, anual — e faça a seguinte operação:</p>
          <ol>
          <li>Reúna os salários da equipe de marketing, custo com mídia, propaganda, ferramentas, etc. Some-os aos salários, comissões e tecnologias da equipe de vendas.</li>
          <li>Divida então esse montante pelo número de clientes adquiridos no período preestabelecido.</li>
          </ol>
          <p>Por exemplo: Se você teve gastos de R$100.000 com marketing e vendas naquele mês, e gerou 50 novos clientes, o seu CAC foi de R$2.000 por cliente.</p>
