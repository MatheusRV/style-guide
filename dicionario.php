          <h4>GPCTCI/BA</h4>
          <p>Siglas que representam o básico de uma metodologia de inbound sales. Cada letra significa uma informação a ser colhida que vai influenciar no processo de qualificação da lead:</p>
          <ul>
          <li><strong>G = Goal: O objetivo de negócios do cliente. Aumentar faturamento, dobrar base de clientes. É a meta dele!</strong></li>
          </ul>
          <ul>
          <li>P = Plan: O que ele está fazendo hoje para chegar no objetivo? Quais são os canais de aquisição e ações de marketing?</li>
          </ul>
          <ul>
          <li>C = Challenges: Os principais desafios, gargalos, problemas, dificuldades que ele enfrenta no momento, e o impedem de conquistar seus objetivos.</li>
          </ul>
          <ul>
          <li>T = Time: Em quanto tempo ele precisa chegar no objetivo (G).</li>
          </ul>
          <ul>
          <li>C = Consequences: O impacto de não atingir aquele objetivo no prazo especificado.</li>
          </ul>
          <ul>
          <li>I = Implications: Quais as mudanças positivas que aconteceriam, caso ele tivesse sucesso.</li>
          </ul>
          <ul>
          <li>B = Budget: Determina se o lead possui caixa para investir na sua solução ou quanto ele está disposto a investir para solucionar seu problema.</li>
          </ul>
          <ul>
          <li>A = Authority: Qual o nível de autoridade da pessoa com quem você está conversando, se ela tem o necessário para tomar uma decisão de compra ou não.</li>
          </ul>
          <p>Esse processo é utilizado na etapa de <strong>qualificação</strong>.</p>
