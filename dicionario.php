          <p>Mas mais do que isso, é uma métrica que mostra se a empresa está atingindo um crescimento saudável, ou se está em situação delicada, pois o churn representa o tamanho da perda financeira causada pelos cancelamentos daquele mês, trimestre ou ano. </p>
