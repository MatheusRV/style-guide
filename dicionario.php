          <h4>Funil de vendas</h4>
          <p>O funil de vendas é um mapeamento do comportamento do seu cliente nas etapas de compra. <strong>Compreender o processo de compra é passo fundamental em direção a um planejamento comercial previsível e escalável</strong>. O funil é normalmente definido em 3 principais etapas:</p>
          <ol>
          <li>Topo do Funil</li>
          <li>Meio</li>
          <li>Fundo</li>
          </ol>
          <p>No <strong>topo do funil</strong> é onde você deve concentrar a maior força para <strong>atrair</strong> pessoas que ainda não conhecem a empresa ou solução, mas estão buscando informações a respeito de problemas comuns ao de seus clientes atuais, ou ideais. </p>
          <p>No <strong>meio do funil</strong>, você começa a filtrar e ter uma menor quantidade de pessoas, mas mais engajadas em descobrir possíveis soluções para um ou mais problemas. </p>
          <p>E, por fim, o <strong>fundo do funil</strong> é quando a pessoa já está decidida em resolver o problema e está pesquisando por quem ou como fará isso.</p>
          <p>O funil de vendas é similar à jornada do cliente, porém mais pensado do ponto de vista da empresa, enquanto a segunda é pensada a partir da perspectiva do cliente. </p>
