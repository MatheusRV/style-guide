          <h3>J</h3>
          <h4>Jornada do Cliente</h4>
          <p>A jornada do cliente é o caminho que a sua persona percorre até o momento de decisão. Do ponto de vista do time de vendas, é importante entender em qual etapa da jornada o prospecto está, para qualificá-lo ou não para o próximo passo da venda. </p>
          <h3>L</h3>
          <h4>Lead</h4>
          <p>Um lead é quando você tem informações únicas de um visitante do seu site. Ele normalmente entrega esses dados por meio de um formulário, para fazer o download de um e-book, acessar promoções ou fazer contato.</p>
          <p>A geração de leads é o principal motor de uma metodologia de inbound marketing.</p>
          <h4>LTV (Lifetime Value)</h4>
          <p>Uma métrica de difícil tradução para a língua portuguesa, mas que significa, resumidamente, o quanto um cliente vale para uma empresa, a projeção de quanto ele vai trazer de faturamento ao longo de seu ciclo de vida enquanto cliente.</p>
          <p><strong>Encontrar este LTV é importante para saber quanto de retorno realmente cada cliente traz</strong>. Para isso, você deve comparar essa métrica com o custo de aquisição de clientes (CAC). Se o LTV estiver abaixo, é preciso realizar mudanças.</p>
          <h3>M</h3>
          <h4>Margem</h4>
          <p>A diferença entre o custo do produto ou serviço e seu preço de venda. O cálculo da margem permite identificar até que ponto você pode dar descontos sem abrir mão do lucro, mesmo que este seja menor.</p>
          <h4>Marketing Qualified Lead (MQL)</h4>
          <p>É um lead que já está mais próximo da etapa de decisão, baseado em seu comportamento em relação a outros. É importante definir quais ações um lead deve tomar para ser considerado qualificado por sua automação de marketing, e então passado para vendas.</p>
          <p>O time de marketing deve ouvir o time de vendas sobre qual é o comportamento de compra que eles estão buscando. Assim, é possível criar conteúdos adequados para todas as etapas do funil de vendas.</p>
          <h4>Monthly Recurring Revenue (MRR)</h4>
          <p>Comumente chamado por sua sigla, MRR é uma métrica que mostra o valor de receita recorrente que uma empresa receberá.</p>
          <p>O principal desafio de empresas que recebem valores recorrentes, como as de Software as a Service (SaaS), é manter o MRR atual, ao mesmo tempo que aumentam esse valor, com novos clientes e aumentando o valor pago pelos atuais (upsell).</p>
          <h3>P</h3>
          <h4>Pipeline</h4>
          <p>Pipeline é como um vendedor se refere as oportunidades que ele tem no momento. Para conquistar <strong>previsibilidade de resultado em vendas</strong>, é preciso analisar o pipe da empresa para perceber como as negociações estão sendo desenvolvidas pela equipe.</p>
          <h4>Pitch de vendas</h4>
          <p>Pitch é como é chamado um discurso de vendas, um argumento que justifica a compra de um produto ou solução.</p>
          <h4>Prospecção</h4>
          <p>É o processo de procurar e identificar potenciais compradores. Existe a prospecção fria, que consiste de ações mais intrusivas, como e-mails ou ligações com pouca ou nenhuma verificação se há algum interesse ou problema por parte do prospecto em ouvir o que você tem a dizer.</p>
          <p>Na metodologia inbound, a prospecção envolve um prévio interesse do prospecto, que você capta gerando conteúdo relevante para ele. Se você acertou a mão na persona, será uma prospecção bem mais efetiva.</p>
          <h3>R</h3>
          <h4>Reunião de Qualificação</h4>
          <p>É a segunda etapa do processo de inbound sales. Depois de realizar a conexão e marcar uma reunião, é preciso levantar todos os pontos necessários para identificar se há uma oportunidade real de fazer negócio, ou não. Reunidas todas as informações e considerando que o lead tem um problema que pode ser resolvido pela sua solução, é marcado um próximo passo, a <strong>reunião de solução</strong>.</p>
          <h4>Reunião de Solução</h4>
          <p>Se o lead passou pela qualificação, a etapa subsequente e que define o inicio do trabalho ou o fim das negociações é a Reunião de Solução. É nela que o vendedor apresenta o produto ou serviço alinhado aos problemas e objetivos do cliente, mostrando como ele vai ajudá-lo.</p>
          <h3>S</h3>
          <h4>Sales Development Representative (SDR)</h4>
          <p>Vide BDR</p>
          <h4>Sales Qualified Lead (SQL)</h4>
          <p>Um SQL é um lead que já interagiu com conteúdos chave definidos junto ao marketing, e foi qualificado pelos critérios do time de vendas para os próximos passos do processo de compra.</p>
          <h4>Service Level Agreement (SLA)</h4>
          <p>SLA é um acordo comum firmado entre duas partes, onde um se compromete a cumprir com certas exigências do outro, e vice-versa.</p>
          <h3>T</h3>
          <h4>Taxa de conversão</h4>
          <p>Taxa de conversão é uma porcentagem que identifica quantos visitantes você tem no site que de fato baixam um eBook e se tornam leads, por exemplo. </p>
          <p>Ou pode ser a taxa de conversão em vendas: Um vendedor que faz 10 reuniões e fecha 1 venda tem uma taxa de conversão de 1%, por exemplo.</p>
          <h4>Ticket Médio</h4>
          <p>O ticket médio significa quanto, em média, um cliente gasta com você. Para calculá-la, você precisa de duas informações:</p>
          <ul>
          <li>Faturamento total de vendas</li>
          <li>Número de novos clientes</li>
          </ul>
          <p>Defina um período (mensal, trimestral ou anual) e divida esse número para ter algo próximo do seu ticket médio. Por exemplo:</p>
          <ul>
          <li>Em um mês, sua empresa faturou R$120.000, vendendo para 70 novos clientes. Portanto, 120.000/70 = R$1.715</li>
          <li>Neste mês em questão, seu ticket médio fora R$1.715 por cliente.</li>
          </ul>
          <h3>U</h3>
          <h4>Upsell</h4>
          <p>Quando um cliente contratou um plano específico, mas contrata novos serviços ou produtos no decorrer do projeto, aumentando o valor mensal pago (MRR), ele realizou um upsell.</p>
          <h3>V</h3>
          <h4>Venda Transacional</h4>
          <p>Ao contrário do modelo recorrente — MRR — uma venda transacional é a forma mais antiga de vender algo. Trata-se de uma negociação de um produto ou serviço pontualmente. </p>
          <p>Normalmente, esse tipo de venda tem um foco maior no produto do que no cliente, ao contrário de modelos de assinatura, e há um risco maior devido a falta de previsibilidade de receita.</p>
          <h4>Vendarketing</h4>
          <p><strong>Vendarketing é o resultado de uma sinergia total entre o time de marketing e o de vendas, que trabalharão em conjunto para gerar cada vez mais negócios para a empresa.</strong></p>
