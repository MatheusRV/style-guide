          <h4>Conexão</h4>
          <p>A conexão é a primeira etapa de um processo de inside sales. O vendedor recebe a lead que passou pelo marketing e foi qualificada, e faz o primeiro contato. Normalmente por telefone, ou e-mail. Existem dois principais <strong>objetivos em uma ligação de conexão</strong>:</p>
          <ol>
          <li>Confirmar que o lead tem um problema que você pode ajudá-lo.</li>
          <li>Marcar uma reunião.</li>
          </ol>
          <p>São ligações rápidas que levam entre 10 a 20 minutos normalmente, sempre com o vendedor tentando identificar uma dor e, caso faça sentido, marcar uma próxima reunião.</p>
          <h4>Conteúdo evergreeen</h4>
          <p>Também chamado de conteúdo perene, refere-se ao tipo de conteúdo mais valioso para uma estratégia de marketing de conteúdo.</p>
          <p>Em tradução literal, evergreen significa "sempre verde". Os conteúdos levam esse nome exatamente porque nunca envelhecem, ou seja, possuem um prazo de validade indefinido ou pelo menos muito longo.</p>
          <p>É um conteúdo para durar no mínimo 2 anos, mas preferencialmente até mais do que isso. Os conteúdos noticiosos, por outro lado, possuem um curto prazo de validade</p>
          <h4>Conversão de topo ou meio de funil</h4>
          <p>É quando um visitante toma a ação de clicar na oferta de conteúdo, normalmente por meio de um botão de call-to-action, e é levado até uma landing page, com um formulário pedindo alguns dados.</p>
          <p>Em troca de seus dados, obtém um material que vai ajudá-lo com algum problema ou dúvida. </p>
          <p><strong>O conteúdo que motivou a conversão do visitante em lead é um ponto extremamente relevante para o time de inbound sales entender qual o problema ele está precisando resolver agora</strong>, e em qual etapa da jornada do cliente ele está.</p>
          <h4>Conversão de fundo de funil</h4>
          <p>Ao contrário da conversão de etapas anteriores da jornada do cliente, essa é uma já focada na última, a decisão. Ou seja, já teve um problema, se educou quanto à resolução e agora procura por alguma empresa para ajudá-lo.</p>
          <p>Normalmente são botões na home do site, como “fale com vendas” ou formulários de contato, orçamento e similares.</p>
          <h4>CRM (Customer Relationship Management)</h4>
          <p>Sigla em inglês para “gerenciamento de relacionamento com clientes”, o CRM é uma boa prática de vendas, mas que hoje normalmente significa uma ferramenta de CRM.</p>
          <p>São softwares que ajudam vendedores a manter o controle do seu pipeline, visualizando suas oportunidades, preenchendo relatórios para análise de performance e gerando dados para avaliação do processo de vendas da empresa.</p>
          <h4>Cross-selling</h4>
          <p>O cross selling é uma técnica de vendas que consiste em vender um produto, baseando-se no sucesso ou funcionalidade de outro. </p>
          <p>O grande trunfo disso é que seu cliente já disse “sim” para o que você estava negociando, permitindo que você ofereça outros produtos ou serviços, que poderão contribuir para a experiência de uso dele como um todo, sem que você pareça estar forçando a barra ou empurrando algo, pois o foco está no cliente!</p>
          <p>Assim, você aumenta o ganho médio por venda, ao mesmo tempo que entrega mais valor na experiência de compra. Um exemplo de cross-selling é vender um celular e oferecer uma capa protetora ou um serviço de seguro contra roubo.</p>
          <h4>CTA - Call to action</h4>
          <p>O CTA é algo (imagem ou texto) que vai induzir um visitante a tomar uma determinada ação. Este CTA  pode ser utilizado em forma de imagem ou texto, e seu objetivo é sempre realizar uma conversão de um usuário ou incentivá-lo a interagir com algum outro tipo e formato de conteúdo.</p>
