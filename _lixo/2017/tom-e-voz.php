<h2>Tom e voz: quem é a Rock Content?</h2>
<h3>Primeiramente: quem queremos ser?</h3>
<p>Queremos ser autoridade e referência quando se fala de Marketing de Conteúdo no Brasil. Buscamos objetividade e simplicidade, sempre com muito conteúdo de qualidade. Valorizamos muito nosso senso de humor e a liberdade que temos com nossos leitores, redatores, clientes e time. Somos divertidos, mas não forçamos piadas (ninguém quer ler um texto escrito pelo tio do pavê!). Nós deixamos que elas apareçam naturalmente.</p>
<p>Queremos ensinar e educar sem enrolações.</p>
<h3>Tom e voz</h3>
<h4>A voz da Rock Content</h4>
<p>Nossa voz é empolgada, amigável, informal e objetiva. Nós somos:</p>
<ul>
<li>Informais, sem perder a autoridade;</li>
<li>Divertidos, sem perder a objetividade, seriedade e profissionalismo;</li>
<li>Didáticos! Adoramos ensinar;</li>
<li>Confiantes, mas não arrogantes;</li>
<li>Amigáveis, sem ultrapassar os limites do bom senso;</li>
<li>Provocativos, querendo levar o leitor a tomar um próximo passo;</li>
<li>Claros como a água (David Allen estaria orgulhoso!);</li>
<li>Sempre abertos a novas sugestões.</li>
</ul>
<h4>O tom da Rock Content</h4>
<p>Há uma grande diferença entre voz e tom (novamente citando o Mailchimp): <em>Pense da seguinte maneira: você tem sempre a mesma voz, mas seu tom é algo que está sempre mudando, dependendo do contexto e da pessoa com a qual você está falando.</em></p>
<p><strong>Nosso tom, apesar de seguir a nossa voz, é sempre ser <strong>flexível. </strong>O contexto, a pessoa que está lendo e seus sentimentos devem sempre ser considerados e tidos como guia para o tom que você vai usar, independente do conteúdo que você está produzindo. Se você ficar na dúvida sobre qual tom usar, pergunte a si mesmo: <em>esse conteúdo resolveu o problema de alguém? S</em>e a resposta for sim, você está no caminho certo.</strong></p>
