                            <h4>Promoção de materiais ricos</h4>
                            <p>Este tipo de post existe para a divulgação de algum material rico que produzimos, como ebooks, whitepapers ou webinars.</p>
                            <p>Esse formato de post deve ser objetivo. Ele fala em poucas palavras o assunto do material rico, como ele vai ajudar seu leitor e dá exemplos de alguns conteúdos que ele encontrará dentro do ebook.</p>
                            <p>Confira um exemplo desse formato de post <a href="http://marketingdeconteudo.com/seo-o-guia-definitivo/">aqui</a>.</p>
