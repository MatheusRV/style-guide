                    </section>
                    <section id="o-q-fazer-nao-fazer">
                        <h3>O que fazer e não fazer</h3>
                        <p>Ao escrever para o Marketing de Conteúdo, tenha em mente alguns requisitos essenciais:</p>
                        <ul>
                            <li>
                                <p>Nossos posts não são frios e formais. Escreva pensando no seu texto como uma conversa franca com o leitor.</p>
                            </li>
                            <li>
                                <p>Gírias são permitidas, contanto que elas sejam de conhecimento geral do público brasileiro. Evite regionalismos e tenha bom senso.</p>
                            </li>
                            <li>
                                <p>Evite frases e parágrafos longos. Leia seu texto em voz alta. Precisa parar para respirar? Se a resposta for sim, sua frase está longa demais.</p>
                            </li>
                            <li>
                                <p>Você não é a Wikipedia. Seja explicativo, mas não seja robótico. </p>
                            </li>
                            <li>
                                <p>Está usando um termo técnico, siglas ou palavras estrangeiras? Explique seu significado.</p>
                            </li>
                            <li>
                                <p>Releia, revise, reescreva. Não aceitamos erros de ortografia. E quando eles ocorrem, corrigimos na velocidade da luz.</p>
                            </li>
                        </ul>
                        <ul>
                            <li>
                                <p>Sempre use &ldquo;Nós, da Rock Content, fazemos assim (...)&rdquo;. </p>
                            </li>
                            <li>
                                <p>Faça uma referência direta ao seu leitor durante o texto. Use &ldquo;você&rdquo; e pense que a pessoa com quem você quer conversar está sentada à sua frente. Não se distancie do seu leitor.</p>
                            </li>
                            <li>
                                <p>Religião, política e assuntos polêmicos: nós realmente precisamos te dizer para não falar sobre isso no seu texto?</p>
                            </li>
                        </ul>
                    </section>
                    <section id="gramatica-ortografia">
                        <h3>Gramática e ortografia</h3>
                        <p>Use o bom senso e sempre revise seu texto. Ainda melhor: use o bom senso e as boas ferramentas que a internet proporciona.</p>
                        <p>Nós somos uma empresa que produz conteúdo — ainda assim, sabemos que eventualmente vamos cometer erros gramaticais e/ou ortográficos.</p>
                        <p>Nós trabalhamos duro para evitá-los e revisamos cada artigo à exaustão. Algumas ferramentas úteis para isso são:</p>
                        <ul>
                            <li>
                                <p>Dicionários on-line gratuitos — aqui na Rock Content nós usamos o <a href="http://www.priberam.pt/DLPO/Default.aspx">Priberam</a> e o <a href="http://www.aulete.com.br">Aulete</a>.</p>
                            </li>
                        </ul>
                        <p>Porém, quando os erros acontecem, temos que:</p>
                        <ul>
                            <li>
                                <p>Ser rápidos e corrigir;</p>
                            </li>
                            <li>
                                <p>Nos desculpar com quem apontou o problema.</p>
                            </li>
                        </ul>
                    </section>
                    <section id="uso-imagens-referencias">
                        <h3>Uso de imagens e referências</h3>
                        <p>As imagens do nosso blog vêm de bancos de imagens pagos que assinamos e possuem autorização para uso editorial. Se você está usando outra imagem, não se esqueça de dizer onde a encontrou. Dê preferência a imagens Creative Commons e sempre faça a atribuição necessária. </p>
                    </section>
                    <section id="links">
                        <h3>Links internos e externos</h3>
                        <p>Fazemos linkagem em todos os nossos posts. Mas não exagere: faça links apenas em palavras-chave que façam sentido e ajudem o leitor.</p>
                        <p>Se você está escrevendo sobre o Facebook, por exemplo, faria sentido colocar um link que direcione para um post falando sobre como conseguir conseguidores no Twitter? A resposta é não. </p>
                        <p>Faça links internos pensando na experiência do usuário: os links devem complementar a leitura e oferecer mais informações relevantes sobre o assunto. &nbsp;</p>
                        <p>Não force a barra e não coloque links demais. Sugerimos que o número de links internos seja pensado de acordo com o número de palavras do post. Se um post tem 2 mil palavras, por exemplo, cerca de 10 links é um número razoável. Um post de mil palavras poderia ter cerca de 5 links, e por aí vai.</p>
                        <p>Mas não veja esses números como regras: eles são apenas um guia. O mais importante é que nossos links façam sentido com o conteúdo proposto. Procure sempre balancear links internos e externos. Em caso de dúvida, linke sempre para o melhor conteúdo disponível sobre o assunto, seja interno ou externo.</p>
                    </section>
                    <section id="seo">
                        <h3>SEO</h3>
                        <p>Utilizamos o princípio de escaneabilidade para nossos posts. Ou seja: sabemos que a maioria dos usuários apenas &ldquo;escaneiam&rdquo; o texto, lendo os títulos, subtítulos, imagens e palavras destacadas. Por isso otimizamos nossos textos para facilitar a leitura da seguinte maneira:</p>
                        <ul>
                            <li>
                                <p>Use parágrafos curtos. Blocos grandes de texto são cansativos; </p>
                            </li>
                            <li>
                                <p>Use subtítulos: dessa maneira o leitor sabe exatamente do que se trata aquela seção e pode escolher facilmente qual parte do texto o interessa;</p>
                            </li>
                            <li>
                                <p>Destaque palavras-chave e fatos importantes em negrito, para que elas chamem a atenção dos leitores;</p>
                            </li>
                            <li>
                                <p>Se o conteúdo permitir, escreva-o em forma de lista: elas são muito mais fáceis de ler e são facilmente compreendidas pelo leitor;</p>
                            </li>
                            <li>
                                <p>Sempre que possível use imagens: elas quebram a monotonia do texto e chamam a atenção do leitor.</p>
                            </li>
                        </ul>
                    </section>
                </section>
                <section id="fatores-consideramos">
                    <h2>Fatores on page que consideramos</h2>

                    <p>Os fatores on page são aqueles que podem ser trabalhados dentro do próprio site ou página, e que são fundamentais para que os mecanismos de busca percebam que seu conteúdo está relacionado aos termos buscados pelos usuários.</p>
                    <p>Usamos o próprio Wordpress para essa tarefa: selecionamos as palavras-chave do post (que idealmente deverão aparecer no título principal — H1 —, na URL, no título para SEO e na metadescrição). </p>
                    <section id="titulo-pagina">
                        <h3>Título da página</h3>
                        <p>O título da página (ou title) é a propriedade do código HTML, que identifica aquela frase que aparece na página de resultados dos mecanismos de busca ou na parte de cima do navegador. Ao contrário do que muitas pessoas pensam, esse title não é exatamente o título do artigo.</p>
                        <p>Esse fator é um dos mais importantes quando pensamos em SEO. Por isso, é preciso descrever de forma precisa os assuntos que serão abordados naquela página. Muitas empresas caem no erro de colocar algum slogan ou frases pouco descritivas do negócio — esse tipo de prática influencia diretamente no rankeamento do seu site ou blog.</p>
                        <p>É fundamental trabalhar com um máximo de 63 caracteres, além de se atentar à ordem em que as palavras vão aparecer: as primeiras têm mais relevância para os mecanismos de busca.</p>
                        <p>Por isso, lembre-se sempre de:</p>
                        <ul>
                            <li>
                                <p>não colocar termos separados por vírgula;</p>
                            </li>
                            <li>
                                <p>relacionar o título ao conteúdo da página;</p>
                            </li>
                            <li>
                                <p>trabalhar com um título único por página.</p>
                            </li>
                        </ul>
                    </section>
                    <section id="meta-description">
                        <h3>Meta description ou metadescrição</h3>
                        <p>Apesar de não ser um fator que influencia diretamente no rankeamento daquela página, a meta description é um código que tem como função explicar para o usuário qual é o conteúdo abordado naquela página e, com isso, atraí-lo para a leitura do conteúdo. Pensando em meta description, esse é um fator que influencia consideravelmente na taxa de cliques do seu site — por isso é fundamental que ela siga algumas particularidades recomendadas para atingir bons resultados para nossas páginas. São eles:</p>
                        <ul>
                            <li>
                                <p>Ter, no máximo, 160 caracteres;</p>
                            </li>
                            <li>
                                <p>Ser chamativa e ter grande poder persuasivo. Lembre-se de que esse texto pode definir de forma brusca a quantidade de cliques que sua página vai receber!</p>
                            </li>
                        </ul>
                    </section>
                    <section id="nome-alt-imagens">
                        <h3>Nome e alt text das imagens</h3>
                        <p>O Google não consegue enxergar a imagem da mesma forma que o usuário. Ele precisa de elementos textuais para entender do que se trata e apresentá-la como resultado em alguma busca. Por isso é preciso que, ao subir uma imagem para nosso blog ou site, você se atente a três elementos:</p>
                        <ul>
                            <li>
                                <p>o nome do arquivo;</p>
                            </li>
                            <li>
                                <p>o texto alternativo;</p>
                            </li>
                            <li>
                                <p>o contexto.</p>
                            </li>
                        </ul>
                        <p>É fundamental que o recurso textual das imagens contenha informação capaz de descrevê-las. Evite salvar imagens como &ldquo;794743.jpg&rdquo;. Sempre defina o assunto da imagem no título — exemplo: &ldquo;flores-amarelas.jpg&rdquo; —, para facilitar a leitura.</p>
                        <p>A imagem deve ser inserida junto ao conteúdo com o qual ela se relaciona. Não inclua uma imagem utilizando um Javascript ou CSS, pois os mecanismos de busca utilizam o seu HTML para identificar o assunto do texto, a imagem e a relação entre esses dois elementos.</p>
                    </section>
                    <section id="url-pagina">
                        <h3>URL da página</h3>
                        <p>O endereço da página também é um dos elementos primordiais on page que influenciam no rankeamento do seu site: o Google identifica palavras-chave também na URL. Por isso, assim como nas imagens, é fundamental que ela seja descritiva e apresente o conteúdo daquela página.</p>
                        <p>Quanto mais amigável a URL, melhor!</p>
                    </section>
                    <section id="headings-subtitulos">
                        <h3>Headings ou subtítulos</h3>
                        <p>Os headings são fundamentais para identificar a prioridade dos conteúdos existentes na página. No código, é possível observar até seis headings, apresentando o conteúdo mais importante (h1) e seus subtítulos (h2 a h6).</p>
                        <p>Em geral, o título de um post é o H1, identificado no código por &lt;h1&gt; título do blog post &lt;/h1&gt;. Se estiver editando um post para nosso blog tenha bastante cuidado com esse detalhe. </p>
                    </section>
                </section>
                <section id="concluindo">
                    <h2>Conclusão</h2>

