                        <h3>Tipos de conteúdo</h3>
                        <!-- <section id="posts-educativos"> -->
                            <h4>Posts educativos</h4>
                            <p>São os posts mais comuns do nosso blog. Eles são, em sua maioria, explicações de conceitos e uma forma de esclarecer dúvidas e resolver problemas dos nossos leitores. </p>
                        <!-- </section>
                        <section id="guias"> -->
                            <h4>Guias</h4>
                            <p>São posts completos, extensos (eles possuem mais de 2000 palavras, no geral), que buscam explicar com passos detalhados o funcionamento de algo para nossos leitores. Sua linguagem é didática e objetiva, sempre com imagens que facilitem a visualização de tudo que estamos explicando.</p>
                            <p>Alguns bons exemplos que temos desse formato de post são nossos artigos sobre o <a href="http://marketingdeconteudo.com/google-analytics/">Google Analytics</a> e o <a href="http://marketingdeconteudo.com/google-search-console/">Google Search Console</a>. </p>
                        <!-- </section>
                        <section id="fala-cmo"> -->
                            <h4>Fala, CMO</h4>
                            <p>Série de entrevistas com diversos profissionais do mercado sobre estratégias, desafios e a rotina de suas empresas.</p>
                        <!-- </section>
                        <section id="infograficos"> -->
                            <h4>Infográficos</h4>
                            <p>Conteúdo visual produzido pelo nosso time de design, com temas variados. A linguagem aqui é a mais simples possível: textos muito longos ou complicados dificultam a sua visualização. &nbsp;Um bom exemplo para visualizar esse tipo de post é nosso infográfico sobre o <a href="http://marketingdeconteudo.com/roi-das-midias-sociais/">ROI das mídias sociais</a>. </p>
                        <!-- </section>
