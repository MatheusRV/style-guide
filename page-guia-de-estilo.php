                    <p>Há algum tempo sentimos uma grande necessidade de definir a identidade editorial da Rock Content. Então, inspirados no incrível <a href="http://mailchimp.com/about/style-guide/" target="_blank">guia de estilo da MailChimp</a>, decidimos criar o nosso próprio guia de estilo! Mostrar nossa cara e as coisas que valorizamos, para exibi-las em cada pedacinho de conteúdo que publicamos.</p>
