          <p>O tom dos emails é sempre informal, como se o remetente estivesse conversando com o destinatário. Por isso, todos os emails começam com um cumprimento, como “<em>Oi, tudo bem?</em>”, e terminam um “<em>Um abraço,</em> (<em>nome do remetente)”</em>. Frequentemente, adicionamos <em>tokens de personalização</em> que incluem o nome do destinatário no corpo do email (por exemplo, <em>“Oi </em><em>Clara</em><em>, tudo bem?</em>”). </p>
          <p>As pessoas que recebem nossos emails já possuem um conhecimento básico sobre Marketing Digital. Por isso, a linguagem dos emails é carregada em jargões como “métricas”, “leads”, “inbound”, etc. </p>
          <p>Além disso, nossos emails são mais objetivos que educativos. Ou seja, a intenção é <strong>passar uma mensagem de maneira eficiente para que o destinatário entenda qual deve ser o próximo passo que ele deveria tomar</strong>, como baixar um ebook ou ler um artigo, por exemplo. Por isso, nossos emails possuem, em média, de 3 a 5 parágrafos curtos. </p>
          <p>O layout dos emails enviados é extremamente simples. Não utilizamos templates predeterminados, e nossos emails geralmente contém apenas texto. Periodicamente, utilizamos imagens para chamar atenção para a oferta que estamos fazendo, ou GIFs para adicionar humor à nossa comunicação.</p>
          <h3>Emails do Escreva Para Web</h3>
          <p>De forma geral, os emails do EPW têm uma linguagem bem simples e próxima do leitor. Muitas vezes, chegamos a fazer perguntas e afirmações que nos colocam no lugar do nosso público para criar uma identificação maior. </p>
          <p>Uma das partes mais importantes de qualquer email que é disparado pela equipe do Escreva Para Web é o assunto. Por ser o primeiro contato do público com o conteúdo que estamos divulgando, é a parte com a qual mais nos preocupamos e dedicamos tempo pensando. Devemos ser sempre instigantes e, ao mesmo tempo, honestos com o leitor.</p>
          <p>Os emails disparados para a base do Escreva Para Web são normalmente de três possíveis categorias:</p>
          <h4>Divulgação de materiais</h4>
          <p>Os emails de divulgação de materiais são aqueles que apresentam para a base algum dos nossos materiais ricos: ebooks, cartilhas, webinars ou qualquer outro tipo de conteúdo desse tipo. </p>
          <p>São emails mais curtos, entre 200 e 300 palavras e, normalmente, são apenas textuais. O objetivo desse tipo de email é fazer com que o máximo de pessoas da base veja e interaja com o conteúdo que estamos divulgando.</p>
          <h4>Newsletters</h4>
          <p>As newsletters são emails periódicos enviados para toda a base de pessoas que escolheu receber nossas comunicações via email. A newsletter principal do EPW é a “Rocking News”, que é enviada quinzenalmente e tem um tema por edição, para que a nossa curadoria de conteúdos seja melhor organizada e orientada.</p>
          <p>Já a outra newsletter que temos, é a “Rocking Freela” que compartilha com a base de freelancers ativos quem foi o freela que mais se destacou positivamente no mês. Ou seja, são edições mensais que contam um pouco mais da história do freelancer, além de algumas dicas que essa pessoa tem a oferecer para os demais freelancers.</p>
          <h4>Comunicados oficiais sobre a Plataforma</h4>
          <p>Este tipo de email é enviado principalmente para a base de freelancers ativos. São enviados para notificá-los sobre mudanças e melhorias que fazemos na Plataforma para aprimorar o próprio fluxo de trabalho dos freelancers. </p>
          <p>Não temos periodicidade certa para esses emails pois eles acontecem sob demanda, de acordo com as mudanças que fazemos na Plataforma.</p>
